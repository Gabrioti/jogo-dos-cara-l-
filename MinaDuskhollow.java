    // ENUMS
    // ═══════════════════════════════════════════════════════
    enum Direcao {
        NORTE, SUL, LESTE, OESTE, BAIXO, CIMA;

        static Direcao fromString(String s) {
            return switch (s.toLowerCase().trim()) {
                case "norte", "n"           -> NORTE;
                case "sul",   "s"           -> SUL;
                case "leste", "l", "e"      -> LESTE;
                case "oeste", "o", "w"      -> OESTE;
                case "baixo", "b", "descer" -> BAIXO;
                case "cima",  "c", "subir"  -> CIMA;
                default -> null;
            };
        }

        String pt() {
            return switch (this) {
                case NORTE -> "Norte"; case SUL   -> "Sul";
                case LESTE -> "Leste"; case OESTE -> "Oeste";
                case BAIXO -> "Baixo"; case CIMA  -> "Cima";
            };
        }
    }

    enum EstadoJogo { JOGANDO, VITORIA, DERROTA_MORTE, DERROTA_LOUCURA }

    // ═══════════════════════════════════════════════════════
    // ITEM
    // ═══════════════════════════════════════════════════════
    static class Item {
        final String id, nome, descricao, descUso;
        final boolean consumivel;

        Item(String id, String nome, String descricao, String descUso, boolean consumivel) {
            this.id = id; this.nome = nome;
            this.descricao = descricao; this.descUso = descUso;
            this.consumivel = consumivel;
        }

        @Override public String toString() { return nome; }
    }

    // ═══════════════════════════════════════════════════════
    // NPC
    // ═══════════════════════════════════════════════════════
    static class NPC {
        final String nome, descricao;
        private final List<String> falas;
        private int idx = 0;

        NPC(String nome, String descricao, String... falas) {
            this.nome = nome; this.descricao = descricao;
            this.falas = Arrays.asList(falas);
        }

        String falar() {
            if (falas.isEmpty()) return nome + " não responde.";
            String f = falas.get(idx);
            idx = Math.min(idx + 1, falas.size() - 1);
            return "\"" + f + "\"";
        }
    }

    // ═══════════════════════════════════════════════════════
    // SALA
    // ═══════════════════════════════════════════════════════
    static class Sala {
        final String id, nome, descricao;
        final Map<Direcao, Sala> saidas = new HashMap<>();
        final List<Item> itens = new ArrayList<>();
        NPC npc;
        boolean bloqueada = false;
        String motivoBloqueio = "";
        int danoSanEntrada = 0;
        boolean visitada = false;

        Sala(String id, String nome, String descricao) {
            this.id = id; this.nome = nome; this.descricao = descricao;
        }

        void ligar(Direcao d, Sala s) { saidas.put(d, s); }

        Item itemPorNome(String busca) {
            return itens.stream()
                .filter(i -> i.nome.toLowerCase().contains(busca.toLowerCase())
                          || i.id.equalsIgnoreCase(busca))
                .findFirst().orElse(null);
        }
    }

    // ═══════════════════════════════════════════════════════
    // JOGADOR
    // ═══════════════════════════════════════════════════════
    static class Jogador {
        int vida = 100, vidaMax = 100;
        int sanidade = 100;
        final List<Item> inventario = new ArrayList<>();
        Sala salaAtual;

        // Flags de progresso — enigmas
        boolean enigma1Resolvido = false; // câmara selada aberta (EMAP)
        boolean enigma2Resolvido = false; // tocha acesa com óleo (altar)
        boolean enigma3Resolvido = false; // ordem dos sinos (scriptorium)
        boolean enigma4Resolvido = false; // espelhos posicionados (câmara dos ecos)
        boolean enigma5Resolvido = false; // inscrição latina decifrada (câmara das oferendas)
        boolean enigma6Resolvido = false; // senha do guardião (cripta)

        boolean tochaAcesa       = false;
        boolean espelhoLeste     = false; // espelho girado para leste
        boolean espelhoNorte     = false; // espelho girado para norte

        boolean temItem(String id) {
            return inventario.stream().anyMatch(i -> i.id.equals(id));
        }

        Item getItem(String id) {
            return inventario.stream().filter(i -> i.id.equals(id)).findFirst().orElse(null);
        }

        Item getItemPorNome(String busca) {
            return inventario.stream()
                .filter(i -> i.nome.toLowerCase().contains(busca.toLowerCase())
                          || i.id.equalsIgnoreCase(busca))
                .findFirst().orElse(null);
        }

        String barra() {
            int vp = Math.max(0, Math.min(10, vida * 10 / vidaMax));
            int sp = Math.max(0, Math.min(10, sanidade / 10));
            String bv = "█".repeat(vp) + "░".repeat(10 - vp);
            String bs = "▓".repeat(sp) + "░".repeat(10 - sp);
            return String.format("SAÚDE [%s] %3d | SANIDADE [%s] %3d", bv, vida, bs, sanidade);
        }
    }

