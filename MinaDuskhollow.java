        // LOOP PRINCIPAL
        // ──────────────────────────────────────────
        void iniciar() {
            construirMundo();
            titulo();
            intro();
            sc.nextLine();

            while (estado == EstadoJogo.JOGANDO) {
                imprimirSala();
                System.out.print("\n> ");
                String linha = sc.nextLine().trim().toLowerCase();
                if (linha.isEmpty()) continue;

                String[] partes = linha.split(" ", 2);
                String cmd = partes[0];
                String arg = partes.length > 1 ? partes[1].trim() : "";

                processarComando(cmd, arg);

                if (jogador.vida <= 0)     estado = EstadoJogo.DERROTA_MORTE;
                if (jogador.sanidade <= 0) estado = EstadoJogo.DERROTA_LOUCURA;
            }

            imprimirFim();
        }

        // ──────────────────────────────────────────
        // PROCESSAMENTO DE COMANDOS
        // ──────────────────────────────────────────
        void processarComando(String cmd, String arg) {
            switch (cmd) {
                case "ir","mover","andar"         -> cmdIr(arg);
                case "norte","n"                  -> cmdIr("norte");
                case "sul","s"                    -> cmdIr("sul");
                case "leste","l","e"              -> cmdIr("leste");
                case "oeste","o","w"              -> cmdIr("oeste");
                case "baixo","b","descer"         -> cmdIr("baixo");
                case "cima","c","subir"           -> cmdIr("cima");
                case "pegar","p","coletar"        -> cmdPegar(arg);
                case "examinar","ex","olhar","ver"-> cmdExaminar(arg);
                case "usar","u"                   -> cmdUsar(arg);
                case "inventario","inv","i"       -> cmdInventario();
                case "falar","f","conversar"      -> cmdFalar();
                case "atacar","a","lutar"         -> cmdAtacar();
                case "mapa"                       -> cmdMapa();
                case "status","stats"             -> cmdStatus();
                case "ajuda","help","?"           -> cmdAjuda();
                case "sair","quit"                -> {
                    System.out.println("Você abandona a missão. Greymoor ficará sem resposta.");
                    estado = EstadoJogo.DERROTA_MORTE;
                }
                default -> System.out.println("Comando não reconhecido. Digite 'ajuda' para ver os comandos.");
            }
        }

        // ──────────────────────────────────────────
        // COMANDO: IR
        // ──────────────────────────────────────────
        void cmdIr(String arg) {
            Direcao dir = Direcao.fromString(arg);
            if (dir == null) {
                System.out.println("Para onde? (norte / sul / leste / oeste / cima / baixo)");
                return;
            }
            Sala atual   = jogador.salaAtual;
            Sala destino = atual.saidas.get(dir);
            if (destino == null) {
                System.out.println("Não há caminho nessa direção.");
                return;
            }

            // ── BLOQUEIO: câmara selada requer código ──
            if (destino.bloqueada && destino.id.equals("camaraSelada")) {
                System.out.println("⚠  " + destino.motivoBloqueio);
                System.out.print("\n  Digite o código (4 letras) ou ENTER para cancelar: ");
                String codigo = sc.nextLine().trim().toUpperCase();
                if (codigo.equals("EMAP")) {
                    System.out.println("  O disco gira. A trava cede com um baque metálico.");
                    sep();
                    System.out.println("  ╔══════════════════════════════════════════════╗");
                    System.out.println("  ║  ENIGMA 1 RESOLVIDO — SIGILO DO CAPATAZ     ║");
                    System.out.println("  ║  Câmara aberta! O nível profundo aguarda.    ║");
                    System.out.println("  ╚══════════════════════════════════════════════╝");
                    destino.bloqueada = false;
                    jogador.enigma1Resolvido = true;
                    jogador.sanidade = Math.min(100, jogador.sanidade + 5);
                } else {
                    System.out.println("  Código incorreto. A porta não cede.");
                    return;
                }
            }

            // ── BLOQUEIO: câmara das oferendas requer chave ──
            if (destino.bloqueada && destino.id.equals("camaraOferendas")) {
                if (jogador.temItem("chaveBronze")) {
                    System.out.println("  Você usa a Chave de Bronze na fechadura.");
                    System.out.println("  A grade cede com um rangido profundo. Além, escuridão.");
                    destino.bloqueada = false;
                } else {
                    System.out.println("⚠  " + destino.motivoBloqueio);
                    return;
                }
            }

            // ── BLOQUEIO: câmara dos ecos → câmara das oferendas requer enigma 4 ──
            if (dir == Direcao.BAIXO && atual.id.equals("camaraEcos") && !jogador.enigma4Resolvido) {
                System.out.println("⚠  O cristal no teto permanece apagado. Algo bloqueia a descida.");
                System.out.println("   Você sente que os espelhos precisam estar na posição certa.");
                System.out.println("   [ Use 'usar espelho esquerdo' e 'usar espelho direito' ]");
                return;
            }

            // ── BLOQUEIO: câmara das oferendas → câmara selada requer enigma 5 ──
            if (dir == Direcao.BAIXO && atual.id.equals("camaraOferendas") && !jogador.enigma5Resolvido) {
                System.out.println("⚠  O piso da câmara permanece fechado. A inscrição no altar aguarda resposta.");
                System.out.println("   [ Use 'usar altar' com a resposta do enigma 5: MEMENTO ]");
                return;
            }

            jogador.salaAtual = destino;

            if (!destino.visitada && destino.danoSanEntrada > 0) {
                int dano = destino.danoSanEntrada;
                // Amuleto reduz dano de sanidade
                if (jogador.temItem("amuleto")) {
                    dano = Math.max(0, dano - 8);
                    System.out.println("\n  O Amuleto de São Vigílio pulsa. A presença recua um pouco.");
                }
                jogador.sanidade = Math.max(0, jogador.sanidade - dano);
                if (dano > 0) {
                    System.out.println("\n  ⚠ Uma presença colossal pesa sobre você.");
                    System.out.println("  O verde pulsa nas paredes. Sanidade: -" + dano);
                    System.out.println("  " + jogador.barra());
                }
            }
        }

